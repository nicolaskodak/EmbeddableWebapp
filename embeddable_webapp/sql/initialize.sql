  FROM norm n
  JOIN tb_mgmt.erp_customers ec
    ON ec.erp_customer_name = n.erp_customer_name_text
  LEFT JOIN tb_mgmt.pos_stores ps
    ON ps.pos_store_name = n.pos_store_name_text
