raw_norm AS (
  SELECT
    NULLIF(TRIM(product_name), '')        AS product_name,
    NULLIF(TRIM(pos_item_name), '')       AS pos_item_name,
    NULLIF(TRIM(pos_option_group), '')    AS pos_option_group,
    NULLIF(TRIM(pos_option_name), '')     AS pos_option_name
  FROM raw
),
opt AS (
  SELECT
    r.*,
    pog.id AS pos_option_group_id,
    pov.id AS pos_option_value_id
  FROM raw_norm r
  LEFT JOIN tb_mgmt.pos_option_group pog
    ON pog.pos_option_group = r.pos_option_group
  LEFT JOIN tb_mgmt.pos_option_value pov
    ON pov.pos_option_value = r.pos_option_name
),
resolved AS (
  SELECT
    pi.id AS pos_item_id,
    p.id  AS product_id,
    o.product_name,
    o.pos_item_name,
    o.pos_option_group,
    o.pos_option_name
  FROM opt o
  JOIN tb_mgmt.pos_items pi
    ON pi.pos_item_name = o.pos_item_name
   AND (
        (o.pos_option_group_id IS NULL AND pi.pos_option_group_id IS NULL)
        OR pi.pos_option_group_id = o.pos_option_group_id
       )
   AND (
        (o.pos_option_value_id IS NULL AND pi.pos_option_value_id IS NULL)
        OR pi.pos_option_value_id = o.pos_option_value_id
       )
  JOIN tb_mgmt.products p
    ON p.product_name = o.product_name
)
